import UIKit

class MoviesResponse: Codable {
    
    let page, totalResults, totalPages: Int?
    let results: [Movie]
    
    enum CodingKeys: String, CodingKey {
        case page
        case totalResults = "total_results"
        case totalPages = "total_pages"
        case results
    }
}
